<?
{

  /*
   * inputs:
   *
   *   day
   *   month
   *   year
   */

  $dob = array();

  if (empty($context->inputs["day"]) == false)
    $dob[] = $context->inputs["day"];
  else
    $dob[] = "";

  if (empty($context->inputs["month"]) == false)
    $dob[] = $context->inputs["month"];
  else
    $dob[] = "";

  if (empty($context->inputs["year"]) == false)
    $dob[] = $context->inputs["year"];
  else
    $dob[] = "";

  print implode(":", $dob);

}
?>
