CREATE TABLE Profiles
(
  `id` BIGINT(64) UNSIGNED NOT NULL,
  `user` BIGINT(64) UNSIGNED NOT NULL,
  `avatar` VARCHAR(128),
  `identity` VARCHAR(128),
  `gender` VARCHAR(16),
  `location` VARCHAR(128),
  `website` VARCHAR(128),
  `signature` TEXT,
  `description` TEXT,
  `dob` VARCHAR(16),
  `access` BIGINT(64) UNSIGNED NOT NULL,

  PRIMARY KEY(`id`),
  UNIQUE KEY(`user`)
) ENGINE = MyISAM;
